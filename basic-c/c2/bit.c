#include <stdio.h>
int main()
{
	int x = 41;			 //00000000 00000000 00000000 00101001
	printf("%d\n", ~x);  //11111111 11111111 11111111 11010110
	printf("%d\n", x);
	printf("%d\n", x<<3);//00000000 00000000 00000001 01001000
	printf("%d\n", x);
	printf("%d\n", x>>3);//00000000 00000000 00000000 00000101
	int y = -42;         //11111111 11111111 11111111 11010110
	printf("%d\n", y>>3);//11111111 11111111 11111111 11111010
	unsigned int z = -42;//11111111 11111111 11111111 11010110
	printf("%u\n", z);   
	printf("%u\n", z>>3);//00011111 11111111 11111111 11111010
	int a=5, b=1234;
	a = a ^ b;
	b = a ^ b;
	a = a ^ b;
	printf("a=%d, b=%d\n", a, b);
	return 0;
}

